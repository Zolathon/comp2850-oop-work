fun main() {
    val myAge = 29u
    val universeAge = 13_800_000_000L
    val status = 'M'
    val name = "Sarah"
    val height = 1.78f
    val root2 = Math.sqrt(2.0)
    println("myAge, ${myAge::class}")
    println("universeAge, ${universeAge::class}")
    println("status, ${status::class}")
    println("name, ${name::class}")
    println("height, ${height::class}")
    println("root2, ${root2::class}")
}